
# ARITHMETIC OPERATIONS
a=10
b=2
a+=b          # a=a+b
print("1:",a)      # a=12
a-=b          # a=a-b
print("2:",a)      # a=10
a/=b          # a=a/b
print("3:",a)      # a=5
a*=b          # a=a.b
print("4:",a)      # a=10
a**=b         # a=a^b
print("5:",a)      # a=100
a//=b         # FLOOR DIVISION--LARGEST INTEGER<=QUOTIENT
print("6:",a)      # a=50
a%=7         # a-(b*(a//b))
print("7:",a)      # a=1
